package WeekdaysSiam;

public class DataTypes {

	public static void main(String[] args) {
		String firstname="Imran";
		String lastname="Ali";
		System.out.println(firstname+" "+lastname);
		
		//Byte-8bit
		byte number=-128;
		byte number2=127;
		
		//short-16bit
		short x=-32_768;
		short y=32_767;
		
		//int 32 bit
		int a=-2_147_483_648;
		int b=2_147_483_647;
		
		//long -64bit-1
		long z=3476594865645646l;
		
		//double-decimal 64 bit -d
		double f=1.33d;
		
		//float 32bit-f
		float g=1.222f;
		
		//char-character-unicode-16bit
		char h='\u00e5';
		
		//boolean-true or false
		boolean s=false;
		System.out.println(s);
		
	}

}
